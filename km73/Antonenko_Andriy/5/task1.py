a = [int(s) for s in input('Введіть елементи списку : ').split( )]
p = int(input('Введіть зріст петрика : '))
if p<100 or p>200:
    print('Вводьте лише значення що лежать в проміжку [100,200]')
for i in range(0,len(a)-1):
    if a[i]<100 or a[i]>200:
        print('Вводьте лише значення що лежать в проміжку [100,200]')
    else:
        if a[i]>=p and a[i+1]<p:
            print( 'Номер Петрика в строю : '+str(i+2))
