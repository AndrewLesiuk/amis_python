def distance(x1, y1, x2, y2):
    return(0.5*((x2-x1)**2+(y2-y1)**2))
print("Програма визначає відстань між точками (x1,y1) і (x2,y2)")
x1 = float(input("Введіть координату х1:"))
y1 = float(input("Введіть координату y1:"))
x2 = float(input("Введіть координату х2:"))
y2 = float(input("Введіть координату y2:"))
print("Відстань між точками:", distance(x1, y1, x2, y2))
